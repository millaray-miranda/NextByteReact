// Funciones auxiliares para trabajar con los productos
export const obtenerProductoPorId = (id) => {
  return productosData.find(producto => producto.id === parseInt(id));
};

export const obtenerProductosPorCategoria = (categoria) => {
  if (categoria === 'todos') return productosData;
  return productosData.filter(producto => producto.categoria === categoria);
};

export const obtenerProductosEnOferta = () => {
  return productosData.filter(producto => producto.enOferta);
};

export const obtenerProductosRelacionados = (productoId, categoria, limite = 4) => {
  return productosData
    .filter(p => p.categoria === categoria && p.id !== productoId)
    .slice(0, limite);
};

